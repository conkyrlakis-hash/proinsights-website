def faq_schema(faq_list):
    if not faq_list:
        return None
    return {
        "@type": "FAQPage",
        "mainEntity": [
            {
                "@type": "Question",
                "name": item["q"],
                "acceptedAnswer": {"@type": "Answer", "text": item["a"]},
            }
            for item in faq_list
        ],
    }


def breadcrumb_schema(items):
    """items: list of (label, url) tuples"""
    return {
        "@type": "BreadcrumbList",
        "itemListElement": [
            {
                "@type": "ListItem",
                "position": i + 1,
                "name": label,
                "item": url if url.startswith("http") else (DOMAIN + url),
            }
            for i, (label, url) in enumerate(items)
        ],
    }


def article_schema(title, description, slug, publish_date, category):
    return {
        "@type": "Article",
        "headline": title,
        "description": description,
        "url": f"{DOMAIN}/blog/{slug}",
        "datePublished": publish_date,
        "dateModified": publish_date,
        "author": {"@type": "Organization", "name": "ProInsights"},
        "publisher": {"@type": "Organization", "name": "ProInsights",
                      "logo": {"@type": "ImageObject", "url": "https://raw.githubusercontent.com/conkyrlakis-hash/proinsights-brand-assets/main/proinsights-light-on-dark.png"}},
        "articleSection": category,
    }


def local_business_schema(city):
    return {
        "@type": "LocalBusiness",
        "name": f"ProInsights — {city}",
        "url": f"{DOMAIN}/cyprus/{('hotel-reputation-management' if 'hotel' in city.lower() else 'restaurant-review-management')}",
        "areaServed": city,
        "address": {"@type": "PostalAddress", "addressCountry": "CY"},
    }


def combine_schema(*items):
    """Combine multiple JSON-LD nodes into a @graph array."""
    valid = [i for i in items if i]
    return {"@context": "https://schema.org", "@graph": valid}


