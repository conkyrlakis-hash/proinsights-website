def write_sitemap(commercial_slugs, blog_slugs, cyprus_slugs, output):
    today = dt.date.today().isoformat()
    urls = []
    # Homepage
    urls.append(("/", "1.0", "weekly"))
    # Commercial pages
    for s in commercial_slugs:
        urls.append((f"/{s}", "0.9", "monthly"))
    # Cyprus pages
    urls.append(("/cyprus/", "0.8", "monthly"))
    for s in cyprus_slugs:
        urls.append((f"/cyprus/{s}", "0.8", "monthly"))
    # Blog
    urls.append(("/blog/", "0.7", "weekly"))
    for s in blog_slugs:
        urls.append((f"/blog/{s}", "0.6", "monthly"))
    # Pricing already in commercial_slugs
    body = "\n".join(
        f'  <url>\n    <loc>{DOMAIN}{u}</loc>\n    <lastmod>{today}</lastmod>\n    <changefreq>{freq}</changefreq>\n    <priority>{pri}</priority>\n  </url>'
        for u, pri, freq in urls
    )
    xml = f'<?xml version="1.0" encoding="UTF-8"?>\n<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n{body}\n</urlset>\n'
    output.write_text(xml, encoding="utf-8")


def write_robots(output):
    body = (
        "User-agent: *\n"
        "Allow: /\n"
        "Disallow: /sample-hotel.html\n"
        "Disallow: /sample-restaurant.html\n"
        "Disallow: /_content/\n"
        "\n"
        f"Sitemap: {DOMAIN}/sitemap.xml\n"
    )
    output.write_text(body, encoding="utf-8")


