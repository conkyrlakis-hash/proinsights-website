def render_commercial_page(slug, content, *, is_cyprus=False):
    """slug: page slug (no leading slash). content: dict from commercial.json."""
    title = content["title"]
    description = content["description"]
    h1 = content["h1"]
    intro = content.get("intro", "")
    body_html = content["body_html"]
    faq_list = content.get("faq", [])
    schema_type = content.get("schema_type", "WebPage")

    url_path = f"/{slug}" if not is_cyprus else f"/cyprus/{slug.replace('cyprus/', '')}"
    canonical_url = DOMAIN + url_path
    css_href = "../design-b.css" if is_cyprus else "design-b.css"

    # Breadcrumb
    if is_cyprus:
        breadcrumb_items = [("Home", "/"), ("Cyprus", "/cyprus/"), (h1.split("|")[0].strip()[:40], url_path)]
    else:
        breadcrumb_items = [("Home", "/"), (h1[:50], url_path)]

    # Schemas
    schema_nodes = [organization_schema(), website_schema()]
    if schema_type == "SoftwareApplication":
        schema_nodes.append(software_application_schema())
    if is_cyprus:
        city = "Cyprus hotels" if "hotel" in slug else "Cyprus restaurants"
        schema_nodes.append(local_business_schema(city))
    if faq_list:
        schema_nodes.append(faq_schema(faq_list))
    schema_nodes.append(breadcrumb_schema(breadcrumb_items))

    schema_json = json.dumps(combine_schema(*schema_nodes), indent=2)
    breadcrumb_html = build_breadcrumb_html(breadcrumb_items)

    # Compose body
    hero = f"""
<section class="hero hero-small">
  <div class="hero-inner narrow">
    <div class="hero-text">
      <h1>{h1}</h1>
      {intro}
      <a href="/#contact" class="btn-dark">Book a demo</a>
      <a href="/sample-restaurant.html" class="btn-ghost">See sample report</a>
    </div>
  </div>
</section>"""

    full_body = hero + body_html + build_faq_html(faq_list) + build_cta_section()

    return PAGE_TEMPLATE.format(
        title=title,
        description=description,
        canonical_url=canonical_url,
        og_type="website",
        css_href=css_href,
        schema_json=schema_json,
        nav_html=NAV_HTML,
        breadcrumb_html=breadcrumb_html,
        body_html=full_body,
        footer_html=FOOTER_HTML,
    )


