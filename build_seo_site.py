def build_breadcrumb_html(items):
    """Visible breadcrumb trail. items = list of (label, url) tuples."""
    if not items or len(items) < 2:
        return ""
    crumbs = []
    for i, (label, url) in enumerate(items):
        if i < len(items) - 1:
            crumbs.append(f'<a href="{url}">{label}</a>')
        else:
            crumbs.append(f'<span aria-current="page">{label}</span>')
    inner = " <span class=\"crumb-sep\">›</span> ".join(crumbs)
    return f'<nav class="breadcrumb" aria-label="Breadcrumb"><div class="breadcrumb-inner">{inner}</div></nav>'


def build_faq_html(faq_list):
    if not faq_list:
        return ""
    items = "\n".join(
        f'<details class="faq-item"><summary>{escape_html(item["q"])}</summary><div class="faq-answer">{item["a"]}</div></details>'
        for item in faq_list
    )
    return f"""
<section class="band band-white">
  <div class="band-inner band-narrow">
    <p class="section-tag">Frequently asked</p>
    <h2 class="h-big">Questions, answered.</h2>
    <div class="faq-list">{items}</div>
  </div>
</section>"""


